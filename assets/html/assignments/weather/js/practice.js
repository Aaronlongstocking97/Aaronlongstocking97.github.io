/*
Create a function to combine two string
There should be a space between the strings 
Output the result to the console.log
*/

// Declare the variables 
let str1 = "Hello";
let str2 = "World";

// Declare the function
function display(str1, str2) {
    // Concatinate the variables together
    let combination = str1 + " " + str2;
    // Log for error checking
    console.log(combination);
}

// Call the function 
display(str1, str2);